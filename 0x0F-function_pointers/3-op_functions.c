#include "3-calc.h"

/**
  * op_add - add
  * @a: int
  * @b: int
  * Return: integer
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - sub
  * @a: int
  * @b: int
  * Return: integer
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - sub
  * @a: int
  * @b: int
  * Return: integer
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - sub
  * @a: int
  * @b: int
  * Return: integer
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - sub
  * @a: int
  * @b: int
  * Return: integer
  */
int op_mod(int a, int b)
{
	return (a % b);
}
